package BookingREST.Stock;

public class Stock {
    private int id;
    private int business_id;
    private int warehouse_id;
    private int product_id;
    private int count;

    public int getId() {
        return id;
    }

    public int getBusiness_id() {
        return business_id;
    }

    public int getWarehouse_id() {
        return warehouse_id;
    }

    public int getProduct_id() {
        return product_id;
    }

    public int getCount() {
        return count;
    }
}
